a = float(input("Enter 1. number: "))
b = float(input("Enter 2. number: "))
operation = input("Enter math operation, for example `+`, `-`, `*` or `/`: ")

if operation == "+":
    print(a + b)
elif operation == "-":
    print(a - b)
elif operation == "*":
    print(a * b)
elif operation == "/":
    print(a / b)